// Given an integer, return an integer with the digits
// reversed.
// --- Examples
//   reverseInt(13) === 31
//   reverseInt(404) === 404
//   reverseInt(100) === 1
//   reverseInt(-13) === -31
//   reverseInt(-100) === -1

function reverseInt(n) {
  // Solution 1:
  // let y = 0,isNegative = false;
  // if(n < 0) {
  //   n *= -1;
  //   isNegative = true;
  // }
  // while(n > 0) {
  //   y = parseInt((y*10) + (n%10));
  //   n = parseInt(n/10);
  // }
  // if(isNegative)
  //   y *= -1;
  // return y;

  //Solution 2:
  let reverse = parseInt(n.toString().split('').reverse().join(''));
  if(n < 0)
    reverse = 0 - reverse;
  return reverse;
}

